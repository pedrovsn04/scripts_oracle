

CREATE SEQUENCE SQ_TESTE   INCREMENT BY 1  START WITH 1 MAXVALUE 200 MINVALUE 0 NOCYCLE  NOCACHE ORDER;


===== Precisando acertar a sequence.

====INCREMENTAR A SEQUENCE    

   
SELECT  SQ_TESTE.NEXTVAL FROM DUAL;
ALTER SEQUENCE SQ_TESTE INCREMENT BY 20; 
SELECT  SQ_TESTE.CURRVAL FROM DUAL; 
   
SELECT  SQ_TESTE.NEXTVAL FROM DUAL;
ALTER SEQUENCE SQ_TESTE INCREMENT BY 1;     
SELECT  SQ_TESTE.CURRVAL FROM DUAL;    
SELECT  SQ_TESTE.NEXTVAL FROM DUAL;


=====
====VOLTAR A SEQUENCE ... 
    
SELECT  SQ_TESTE.CURRVAL FROM DUAL;
ALTER SEQUENCE SQ_TESTE INCREMENT BY -20; 
SELECT  SQ_TESTE.CURRVAL FROM DUAL;
SELECT  SQ_TESTE.NEXTVAL FROM DUAL;
ALTER SEQUENCE SQ_TESTE INCREMENT BY 1; 
SELECT  SQ_TESTE.CURRVAL FROM DUAL;
SELECT  SQ_TESTE.NEXTVAL FROM DUAL;
