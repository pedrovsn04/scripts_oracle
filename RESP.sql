SELECT C.RESPONSIBILITY_NAME FROM APPS.FND_USER_RESP_GROUPS A, APPS.FND_RESPONSIBILITY_TL B,APPS.FND_RESPONSIBILITY_TL C  
WHERE A.RESPONSIBILITY_ID = B.RESPONSIBILITY_ID 
AND B.APPLICATION_ID = A.RESPONSIBILITY_APPLICATION_ID
AND C.RESPONSIBILITY_ID = B.RESPONSIBILITY_ID
AND A.END_DATE IS NULL
AND B.LANGUAGE = 'PTB'
AND A.USER_ID IN (16312,6315,12010,17152,14774,17212,7751,12011,4770,12012,12430,7494,17272,8368,6319,12373,6322,17213,6323,17852,12375)
GROUP BY C.RESPONSIBILITY_NAME;